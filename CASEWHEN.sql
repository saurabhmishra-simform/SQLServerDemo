SELECT * FROM tblEmployee;
--CASE-WHEN CONSIDION
SELECT ID,NAME,SALARY,
	CASE 
		WHEN SALARY>7000 THEN (SALARY*10)/100
		WHEN SALARY>5000 THEN (SALARY*5)/100
		WHEN SALARY>3000 THEN (SALARY*3)/100
		ELSE (SALARY*2)/100
	END AS SGST
FROM tblEmployee;

--CASE EXPRESSION CONDITION
SELECT ID,NAME,SALARY,
	CASE SALARY
		WHEN 7000 THEN (SALARY*10)/100
		WHEN 5000 THEN (SALARY*5)/100
		WHEN 3000 THEN (SALARY*3)/100
		ELSE (SALARY*2)/100
	END AS GRADE FROM tblEmployee;

